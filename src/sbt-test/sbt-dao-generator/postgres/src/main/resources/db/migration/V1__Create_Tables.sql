CREATE TABLE dept (
  DEPT_ID    BIGINT NOT NULL,
  DEPT_NAME  VARCHAR(20),
  VERSION_NO INTEGER,
  PRIMARY KEY(DEPT_ID)
);

CREATE TABLE emp (
  EMP_ID     SERIAL,
  DEPT_ID    INTEGER NOT NULL,
  EMP_NAME   VARCHAR(20),
  HIREDATE   DATE,
  SALARY     NUMERIC(7, 2),
  VERSION_NO INTEGER,
  PRIMARY KEY (EMP_ID)
);

